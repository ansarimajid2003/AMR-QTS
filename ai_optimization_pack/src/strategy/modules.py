class Direction(IntEnum):
    LONG = 1
    SHORT = -1


@dataclass
class Signal:
    """A trade signal generated by a strategy module."""
    timestamp: pd.Timestamp       # When signal was generated
    symbol: str
    direction: Direction
    module: str                   # 'trend', 'meanrev', 'highvol'
    entry_price: float
    stop_loss: float
    take_profit: float
    risk_pct: float = BASE_RISK_PCT
    atr_at_entry: float = 0.0
    regime: Regime = Regime.NEUTRAL
    regime_confidence: float = 0.0
    rr_ratio: float = 0.0        # Reward:Risk ratio
    metadata: dict = field(default_factory=dict)

    def __post_init__(self):
        risk_dist = abs(self.entry_price - self.stop_loss)
        reward_dist = abs(self.take_profit - self.entry_price)
        self.rr_ratio = reward_dist / risk_dist if risk_dist > 0 else 0


@dataclass
class Trade:
    """A completed trade (signal + outcome)."""
    signal: Signal
    exit_price: float
    exit_time: pd.Timestamp
    exit_reason: str              # 'tp', 'sl', 'time', 'regime_change', 'trail'
    pnl_pips: float = 0.0
    pnl_r: float = 0.0           # PnL in R-units
    bars_held: int = 0
    mfe_pips: float = 0.0        # Max favorable excursion
    mae_pips: float = 0.0        # Max adverse excursion

    def __post_init__(self):
        direction = self.signal.direction
        entry = self.signal.entry_price
        risk_dist = abs(entry - self.signal.stop_loss)
        self.pnl_pips = (self.exit_price - entry) * direction * 10000
        self.pnl_r = self.pnl_pips / (risk_dist * 10000) if risk_dist > 0 else 0


