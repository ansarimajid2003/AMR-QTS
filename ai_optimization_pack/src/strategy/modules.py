def compute_indicators(df: pd.DataFrame) -> pd.DataFrame:
    """Add common technical indicators to an OHLC DataFrame."""
    out = df.copy()

    # ATR
    atr_ind = AverageTrueRange(high=df['high'], low=df['low'], close=df['close'],
                               window=ATR_PERIOD, fillna=False)
    out['atr'] = atr_ind.average_true_range()

    # RSI
    rsi_ind = RSIIndicator(close=df['close'], window=MR_RSI_PERIOD, fillna=False)
    out['rsi'] = rsi_ind.rsi()

    # EMAs
    out['ema_fast'] = EMAIndicator(close=df['close'], window=EMA_FAST, fillna=False).ema_indicator()
    out['ema_slow'] = EMAIndicator(close=df['close'], window=EMA_SLOW, fillna=False).ema_indicator()

    # ADX
    adx_ind = ADXIndicator(high=df['high'], low=df['low'], close=df['close'],
                           window=ADX_PERIOD, fillna=False)
    out['adx'] = adx_ind.adx()
    out['plus_di'] = adx_ind.adx_pos()
    out['minus_di'] = adx_ind.adx_neg()

    # Bollinger Bands
    bb = BollingerBands(close=df['close'], window=MR_BB_PERIOD, window_dev=MR_BB_STD, fillna=False)
    out['bb_upper'] = bb.bollinger_hband()
    out['bb_lower'] = bb.bollinger_lband()
    out['bb_mid'] = bb.bollinger_mavg()

    # Bar range
    out['bar_range'] = df['high'] - df['low']
    out['body'] = abs(df['close'] - df['open'])
    out['body_ratio'] = out['body'] / out['bar_range'].replace(0, np.nan)

    # Rolling highs/lows for breakout (shifted by 1 so current bar can break out)
    out['high_20'] = df['high'].rolling(TREND_BREAKOUT_BARS).max().shift(1)
    out['low_20'] = df['low'].rolling(TREND_BREAKOUT_BARS).min().shift(1)

    # Average bar range for expansion checks
    out['avg_range_5'] = out['bar_range'].rolling(5).mean().shift(1)
    out['avg_range_20'] = out['bar_range'].rolling(20).mean().shift(1)

    # ATR change (3-bar smoothed for HighVol module)
    out['atr_change_pct'] = (out['atr'] - out['atr'].shift(3)) / out['atr'].shift(3)

    return out


def detect_h4_structure(h4_df: pd.DataFrame) -> pd.Series:
    """
    Detect H4 structural trend: HH/HL = uptrend, LH/LL = downtrend.
    Returns Series of 1 (uptrend), -1 (downtrend), 0 (no trend).
    """
    highs = h4_df['high']
    lows = h4_df['low']

    # Use 5-bar swing detection
    swing_h = highs.rolling(5, center=True).max() == highs
    swing_l = lows.rolling(5, center=True).min() == lows

    structure = pd.Series(0, index=h4_df.index)

    prev_swing_high = np.nan
    prev_swing_low = np.nan

    for i in range(len(h4_df)):
        if swing_h.iloc[i]:
            if not np.isnan(prev_swing_high):
                if highs.iloc[i] > prev_swing_high:
                    structure.iloc[i] = 1  # Higher high
                elif highs.iloc[i] < prev_swing_high:
                    structure.iloc[i] = -1  # Lower high
            prev_swing_high = highs.iloc[i]

        if swing_l.iloc[i]:
            if not np.isnan(prev_swing_low):
                if lows.iloc[i] > prev_swing_low:
                    structure.iloc[i] = max(structure.iloc[i], 1)  # Higher low
                elif lows.iloc[i] < prev_swing_low:
                    structure.iloc[i] = min(structure.iloc[i], -1)  # Lower low
            prev_swing_low = lows.iloc[i]

    # Forward fill the structure signal
    structure = structure.replace(0, np.nan).ffill().fillna(0).astype(int)
    return structure


